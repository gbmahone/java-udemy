package tratamentoDeExcecoes.application;

import tratamentoDeExcecoes.entities.Reservation;
import tratamentoDeExcecoes.exceptions.DomainException;

import java.text.ParseException;
import java.text.SimpleDateFormat;
import java.util.Date;
import java.util.Scanner;

public class Program {
    public static void main(String[] args) {
        // meu metodo main é um metodo pode lancar uma exceção desse tipo (ParseException), ele não tem que tratar esse tipo de exceção
        // temos duas opções tratar a exceção ou propagar a exceção dentro do codigo usando a palavra "throws"
        Scanner sc = new Scanner(System.in);
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");

        try {
            System.out.print("Room number: ");
            int number = sc.nextInt();
            System.out.print("Check-in date (dd/MM/yyyy): ");
            Date checkIn = sdf.parse(sc.next());
            System.out.print("Check-out date (dd/MM/yyyy): ");
            Date checkOut = sdf.parse(sc.next());

            Reservation reservation = new Reservation(number, checkIn, checkOut);
            System.out.println("Reservation: " + reservation);

            System.out.println();
            System.out.println("Enter data to update the reservation: ");
            System.out.print("Check-in date (dd/MM/yyyy): ");
            checkIn = sdf.parse(sc.next());
            System.out.print("Check-out date (dd/MM/yyyy): ");
            checkOut = sdf.parse(sc.next());


            reservation.updateDates(checkIn, checkOut);
            System.out.println("Reservation: " + reservation);
        }
        catch (ParseException e) {
            System.out.println("Invalid date format");
        }
        catch (DomainException e) {
            System.out.println("Error in reservation: " + e.getMessage());
            //o getMessege vai ser a frase que eu lançei na exceção dentro do metodo updatesDates
        }
        catch (RuntimeException e){
            System.out.println("Unexpected error");
            //qualquer outra exceção
        }
    }
}
